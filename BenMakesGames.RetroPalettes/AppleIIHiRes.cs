using System.Drawing;

namespace BenMakesGames.RetroPalettes;

/// <summary>
/// Apple II hi-res palette.
/// </summary>
public static class AppleIIHiRes
{
    public static readonly Color Black = Color.FromArgb(0, 0, 0);
    public static readonly Color Green = Color.FromArgb(0x43, 0xC3, 0);
    public static readonly Color Purple = Color.FromArgb(0xB6, 0x3D, 0xFF);
    public static readonly Color Orange = Color.FromArgb(0xEA, 0x5D, 0x15);
    public static readonly Color Blue = Color.FromArgb(0x10, 0xA4, 0xE3);
    public static readonly Color White = Color.FromArgb(255, 255, 255);
}